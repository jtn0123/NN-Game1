"""Hand-crafted, physics-verified Crystal-Caves-style levels.

Sixteen ORIGINAL levels authored from scratch in the spirit of Crystal Caves
Episode 1 — NOT copies of the original layouts (those cannot be recovered: a
tile's solidity lives in the game's compiled collision code, not in the level
data or graphics we can read, which is why the reverse-engineered import came
out 0/16 winnable). These match the original's scale and mechanics: the full
40x24 grid, ladders, elevators, colour-keyed lever/door gates, spikes/acid
hazards, crawler/flyer enemies, ammo/treasure/air-tank pickups, and power-ups.

EVERY level is certified winnable by the physics-faithful reachability oracle in
experiments/cc_status/level_reach.py: from the player start, using only the
engine's real walk/jump/climb physics, every crystal, switch, and the exit is
physically reachable. (Enemies and hazards are placed off the critical path so
they never seal the only route.)

Legend: '#' solid  '.' empty  'P' player  'E' exit  '*' crystal  'H' ladder
'=' elevator  's'/'S' switch(red/blue)  'D'/'d' door(red/blue)  '^' spike
'~' acid  'M' crawler  'F' flyer  'A' ammo  '$' treasure  'O' air-tank
'p'/'g'/'z' power-ups.

Regenerate/verify with:  python -m experiments.cc_status.level_reach
"""


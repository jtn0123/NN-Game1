"""Hand-crafted, physics-verified Crystal-Caves-style levels.

Why these exist: the levels reverse-engineered from the original CC1 game data
were provably unplayable (0/16 winnable under real physics) because a tile's
solidity lives in the game's collision code, not in the level data or graphics we
can read — so the byte->tile mapping grossly over-solidified and mislabelled
objects. These are ORIGINAL designs authored by hand in the spirit of Crystal
Caves (collect every crystal, throw switches to open doors, dodge spikes, reach
the exit) — not copies of the original layouts, which cannot be recovered.

There are 16 levels, matching the count of the original Episode 1. EVERY one is
certified winnable by the physics-faithful reachability oracle in
experiments/cc_status/level_reach.py: from the player start, using only the
mechanics this engine actually has (walk / jump / climb), every crystal, switch,
and the exit is physically reachable.

Legend (engine tiles): '#' solid  '.' empty  'P' player  'E' exit  '*' crystal
'H' ladder  's' switch  'D' door  '^' spike.

Regenerate/verify with:  python -m experiments.cc_status.level_reach
"""

